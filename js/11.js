// ! Iteradores en JS

const tecnologia = ['HTML', 'CSS', 'JavaScript', 'React', 'Node.js'];

// ? ForEach - Acceder a cada elemento del Array
const arrayForeach = tecnologia.forEach((tech) => tech);

// ? Map - Crea un nuevo Array
const arrayMap = tecnologia.map((tech) => tech);

console.log(arrayForeach);
console.log(arrayMap);
